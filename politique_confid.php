Données de journal
Nous souhaitons vous informer que chaque fois que vous visitez notre Service, nous collectons des informations que votre navigateur nous envoie, appelées Données de journal. Ces données de journal peuvent inclure des informations telles que l'adresse de protocole Internet ("IP") de votre ordinateur, la version du navigateur, les pages de notre service que vous visitez, l'heure et la date de votre visite, le temps passé sur ces pages et d'autres statistiques.

Biscuits
